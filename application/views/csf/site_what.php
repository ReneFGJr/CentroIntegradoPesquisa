	<h1>O que é?</h1>
</div>
<div class="oquee">
	<p>
		O Programa Ciência sem Fronteiras objetiva propiciar a formação de recursos humanos altamente qualificados nas melhores
		universidades e instituições de pesquisa estrangeiras, com vistas a promover a internacionalização da ciência e da tecnologia
		nacional, estimulando estudos e pesquisas de brasileiros no exterior, inclusive com a expansão significativa do intercâmbio e da mobilidade de graduandos.
	</p>
	<br />
	<p>
		<strong>Objetivos específicos:</strong>
	</p>
	<ul>
		<li>
			oferecer oportunidade de estudo a discentes brasileiros em universidades de excelência, bem como oferecer a possibilidade de
			estágio programado de pesquisa ou inovação tecnológica com acompanhamento;
		</li>
		<li>
			permitir a atualização de conhecimentos em grades curriculares diferenciadas, possibilitando o acesso de estudantes
			brasileiros a instituições de elevado padrão de qualidade, visando a complementar sua formação técnico-científica em áreas
			prioritárias e estratégicas para o desenvolvimento do Brasil;
		</li>
		<li>
			complementar a formação de estudantes brasileiros, dando-lhes a oportunidade de vivenciar experiências educacionais voltadas
			para a qualidade, o empreendedorismo, a competitividade e inovação;
		</li>
		<li>
			estimular iniciativas de internacionalização das universidades brasileiras;
		</li>
		<li>
			possibilitar a formação, com qualidade, de uma força de trabalho técnico-científica altamente especializada.
		</li>
	</ul>
</div>
