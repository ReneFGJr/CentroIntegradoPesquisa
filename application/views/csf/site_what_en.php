	<h1>What is it?</h1>
</div>
<div class="oquee">
	<p>
		The Science without Borders Programme was launched by the Federal Government in 2012. It is managed by the Brazilian higher
		education agencies CAPES (Coordenação de Aperfeiçoamento de Pessoal de Nível Superior) and CNPq (Conselho Nacional de Desenvolvimento Científico e Tecnológico). The programme chiefly aims to provide extended education for highly qualified human resources in the best foreign universities and research institutions in order to promote the internationalisation of national science and technology, and to encourage the studies and research of Brazilians abroad with the significant expansion of exchange experiences and the mobility of graduation students.
	</p>
	<br />
	<p>
		<strong>Specific goals:</strong>
	</p>
	<ul>
		<li>
			To offer Brazilian students the opportunity to study in universities of excellence, and the possibility of completing a programmed internship of research or technological innovation with supervision;
		</li>
		<li>
			To enable the updating of knowledge in distinguished curricula by allowing Brazilian students access to top institutions in order to complement their technical and scientific training in priority and strategic areas for the development of Brazil;
		</li>
		<li>
			To complement the higher education of Brazilian students by giving them the opportunity to benefit from an educational experience that is based on quality, entrepreneurship, competitiveness and innovation;
		</li>
		<li>
			To stimulate the internationalization initiatives of Brazilian universities;
		</li>
		<li>
			To enable the quality education of a highly qualified technical and scientific work force.
		</li>
	</ul>
</div>