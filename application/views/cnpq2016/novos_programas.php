             <h2><b>Novos Programas</b></h2>   <br>
                
                <p>A PUCPR oferece 3 programas diferenciados, a saber:<br>
                    <br>
                
                <h3><b>1)PIBIC/PIBITI Sanduíche - Mobilidade Nacional e Internacional</b></h3><br>
                É uma iniciativa da PUCPR e tem como objetivo geral o aprimoramento da formação de pesquisador dos estudantes participantes dos programas de IC, por meio de uma experiência de pesquisa interinstitucional nacional e/ou internacional, contribuindo diretamente para a incorporação de novos conhecimentos. 
                <br><br>
                O professor proponente de projeto nesta modalidade deve ter título de doutor e estar, preferencialmente, vinculado aos programas de stricto sensu, além de demonstrar cooperação nacional/internacional com instituições de pesquisas renomadas. 
                <br><br>
                O estudante deve ser cuidadosamente selecionado, ter perfil para a pesquisa, maturidade para representar a PUCPR em outra instituição e ter, obrigatoriamente, realizado um ano de participação no programa. 
                <br><br>
                Esta modalidade permite a vivência da pesquisa em outra cultura, o enriquecimento e fortalecimento de network e da ampliação das perspectivas de cooperação científica, o contato com outras realidades, oportunidades e desafios, o que contribui para que o estudante possa fazer a diferença em um mundo globalizado. 
                <br><br>
                O investimento da Universidade, com recursos próprios, é da seguinte ordem:
                <br><br>
                <ul>
                    <li>Bolsa de R$ 400,00 por 12 meses</li>
                    <li>Auxílio mobilidade nacional de R$ 1.300,00 por até 6 meses</li>
                    <li>Auxílio mobilidade internacional de US$ 700 por até 6 meses</li>
                    <li>Passagem aérea e seguro saúde. </li>
                </ul>
                <br><br>
                <h3><b>2)Bolsas de Iniciação Científica e Tecnológica - PIBIC Master – Combined Degree</b></h3><br>
                
                Este programa visa atrair e motivar estudantes talentosos que demonstrem elevada aptidão à pesquisa e promover a integração da graduação com a pós-graduação, com a inserção rápida do estudante no ambiente da pós-graduação stricto sensu. 
                <br><br>
                O professor proponente de projeto nesta modalidade deve ter título de doutor, ser preferencialmente pesquisador produtividade, estar, obrigatoriamente, vinculado aos programas de stricto sensu, além de ter histórico de forte cooperação internacional com instituições de pesquisas renomadas.
                <br><br>
                A seleção cuidadosa do estudante deve contemplar: perfil para a pesquisa, maturidade para representar a PUCPR em outra instituição internacional, ter realizado um ano de participação no programa e ter cursado 50% do seu curso de graduação. 
                <br><br>
                A pesquisa a ser desenvolvida deverá ter a envergadura de um curso de mestrado, pois o objetivo é que ao final do programa o estudante defenda sua dissertação. 
                A bolsa ofertada é de 12 a 36 meses, está destinada a estudantes que já tenham cursado 50 % do seu curso de graduação e que possam se dedicar integralmente as atividades acadêmicas e de pesquisa, pois deverão cursar disciplinas do mestrado em paralelo a graduação. 
                <br><br>
                Um incremento a mais deste programa é a possibilidade de seis meses de mobilidade nacional ou internacional, inserindo o estudante num cenário interinstitucional, em redes de cooperação e propiciando a vivência da pesquisa em uma outra cultura. 
                <br><br>
                O PIBIC Master tem como premissa a pesquisa de qualidade tanto na graduação como na pós-graduação stricto sensu, preparando o nosso corpo discente para um mundo globalizado. Esta modalidade tem aderência às áreas estratégicas, ao projeto Excelência no stricto sensu e visa acelerar o desenvolvimento de pesquisa de alto valor à sociedade.
                <br><br>
                O investimento da Universidade, com recursos próprios, é da seguinte ordem:
                <br><br>
                 <ul>
                    <li>Bolsa de R$ 600,00, com vigência de 12 a 36 meses, até o término do mestrado</li>
                    <li>Auxílio mobilidade nacional de R$ 1.300,00 por até 6 meses</li>
                    <li>Auxílio mobilidade internacional de US$ 700 por até 6 meses</li>
                    <li>Passagem aérea e seguro saúde</li>
                    <li>Isenção de mensalidade no mestrado e doutorado.</li>
                </ul>
                <br><br>
                
                <h3><b>3)Programa Institucional de Bolsas de Empreendedorismo e Pesquisa - PIBEP</b></h3><br>
                Este programa tem como intuito fomentar o espírito empreendedor de estudantes de graduação da PUCPR por meio da germinação de ideias empreendedoras. É uma oportunidade para que estudantes de graduação tirem as suas ideias do papel e a transformem em um negócio de sucesso. O programa foi elaborado com muito rigor e conta com a participação de profissionais altamente renomados no mercado como mentores e como palestrantes em oficinas de capacitação. Desse modo, o estudante não somente terá a oportunidade de criar sua empresa, mas também estará participando de um mini-MBA em empreendedorismo, pesquisa e inovação.
                <br><br>
                O principal objetivo do PIBEP é desenvolver a liderança e o espírito empreendedor nos estudantes de graduação da PUCPR, por meio de ferramentas pedagógicas e de um espírito de "pôr a mão na massa".
                O programa tem aderência aos princípios institucionais do PPI/PDI/2013 da PUCPR no que se refere à Inovação e Empreendedorismo e pretende contribuir para a criação de um ambiente universitário voltado à criatividade, inovação, autonomia e senso crítico.
                <br><br>
                O ciclo do PIBEP é de três meses, ao final desse período as iniciativas que apresentarem grau de inovação, potencial de escalabilidade e perfil empreendedor poderão pleitear a participação no programa de aceleração da HOTMILK - Aceleradora da Agência PUC para transformar a proposta em um negócio de verdade!
                <br><br>
                O investimento da Universidade, com recursos próprios, é até <b>R$ 4.800,00</b> por projeto aprovado.
