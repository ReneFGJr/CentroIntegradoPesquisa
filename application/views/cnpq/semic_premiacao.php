<h1 align="left">FATOR DE CORREÇÃO DA NOTA DO AVALIADOR (CN)</h1>
	
	<p style="width: 700" align="justify" >
		Em anos anteriores foram identificadas discrepâncias nas notas dos avaliadores quando comparada todas 
		as áreas do conhecimento, privilegiando áreas onde os avaliadores são mais amenos na avaliação. De forma, 
		com o intuíto de minimizar o impacto da nota do avaliador foi instituído, desde 2014, o fator de correção (CN). 
		O CN é aplicado a todas as notas dos avaliadores de forma a minimizar as divergências de áreas e a rigorosidade 
		e tolerância de cada avaliado.
	</p>
	<p style="width: 700" align="justify" onmousedown="">
		Outra novidade neste ano é que a nota final para premiação é calculada pela composição de todas as notas, desde 
		a submissão até a apresentação no SEMIC.
	</p>

