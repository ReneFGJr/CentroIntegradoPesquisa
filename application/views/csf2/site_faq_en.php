	<h1>Frequently Asked Questions</h1>
</div>
<h3>How do I enrol in the program?</h3>
<p>
	Applicants should register at the Science without Borders website: <a href="http://www.cienciasemfronteiras.gov.br/web/csf">http://www.cienciasemfronteiras.gov.br/web/csf</a>.
</p>
<p>
	The registration in this website is required for all and any information on student applications to be submitted to the Coordination Office of Science without Borders of the PUCPR (SwB). This way, we can accompany the registration and approval process of each student and answer any further queries.
</p>
<br />
<h3>Who enlists the candidates? The coordinator or the actual candidate? </h3>
<p>
	The candidate is responsible for registering at the government website and at the PUCPR website. The coordinator is responsible for the public calls, deadlines and specific procedures. At the end of the registration process, CAPES/CNPq submits all the applications to the coordinator for approval.
</p>
<br />
<h3>Is my graduation course included in the priority areas? </h3>
<p>
	The list of areas that are considered priorities in the Science without Borders programme is available at the programme website.
</p>
<br />
<h3>Is there a foreign language proficiency requirement?</h3>
<p>
	Which foreign language proficiency examinations are accepted? The requirement is based on the agreement between the agencies (CNPq and CAPES) and the foreign language institute. Read the full text of the call for the country to which you are applying. Each call has its specific rules.
</p>
<br />
<h3>Which overseas universities can participate in the programme?</h3>
<p>
	Top overseas universities signed agreements with the educational representatives (partners) of each country or with the CNPq and CAPES. The partner of each call will provide the list of universities that are participating in the programme.
</p>
<br />
<h3>What are the priority areas that I can apply for?</h3>
<p>
	Topics and areas of interest: Engineering and other technology areas; Exact and Earth Sciences: Physics, Chemistry, Biology and Geosciences, Biomedical and Health Sciences; Computer Science and information technologies; Aerospace Technology; Pharmaceuticals; Sustainable Agricultural Production; Oil, Gas and Coal; Renewable Energies; Mineral Technology; Biotechnology; Nanotechnology and new materials; Technologies of Prevention and Mitigation of Natural Disasters; Biodiversity and Bioprospecting; Marine Science; Creative industry; New Technologies of Constructive Engineering; Training for Technologists. See if there are any restrictions for the priority area in the call of your interest.
</p>
<br />
<h3>Who decides whether the candidate is in the priority areas? </h3>
<p>
	CAPES/CNPq analyze if the applicant's graduation course or field is included in the priority areas.
</p>
<br />
<h3>Should students and researchers who are beneficiaries of the programme adhere to SwB priority areas? </h3>
<p>
	Yes. Both students and researchers should observe the priority areas listed in the programme.
</p>
<br />
<h3>I don´t have the result of my proficiency test. Can I send it later?</h3>
<p>
	Read the schedule of the call to which you are applying carefully and follow the required time limits. It is usually possible to send the proficiency test results after registration.
</p>
<br />
<h3>I have not finished my undergraduate studies, yet. Can I still participate in the programme?</h3>
<p>
	Yes, provided you have completed a minimum of 20% and a maximum of 90% of the course at a Brazilian educational institution and you are properly registered. In this case, students can apply for the Undergraduate Overseas Sandwich (SWG) scholarship for a 6 to 12 month overseas exchange, according to each call.
</p>
<br />
<h3>I'm a PIBIC student, how should I proceed?</h3>
<p>
	You must notify the PIBIC coordination office at the PUCPR about your approval at the destination university to cancel the PIBIC grant. Only then can you accept your SwB scholarship.  CNPq/CAPES does not allow the accumulation of scholarships.
</p>
<br />
<h3>Where do I submit the documents required for registration?</h3>
<p>
	The registration documents requested in the public call should be attached to the registration form provided in "registration forms". All the attached documents should be in PDF format.
</p>
<br />
<h3>Can the required documentation be submitted by e-mail? </h3>
<p>
	No. The required documentation should be attached via the programme page at the website, according to the respective schedule. All documentation should be submitted in PDF format.
</p>
<br />
<h3>I have a PUCPR, ProUni or FIES grant. How should I proceed? </h3>
<p>
	You must notify the SwB coordination office if you receive any financial support to attend a university programme, regardless of whether the grant is from the PUCPR, FIES or ProUNI. This grant will be suspended by the coordination office and the student must report to SIGA to sign a term of "temporary suspension", so the grant is not cancelled and the student can reactivate the grant on his or her return to the PUCPR.
</p>
<br />
<h3>When will I know to what institution I will go? </h3>
<p>
	Students will receive a notice of acceptance via email with the name of the university, and the procedures for the next steps.
</p>
<br />
<h3>Can I choose the overseas university I want to go to? </h3>
<p>
	The intermediaries of the overseas universities and the agencies responsible for the Science without Borders programme will be responsible for applying to the universities according to the priority areas of the Science without Borders programme.
</p>
