		<td colspan=1 width="95%" class="lt2">
		<font class="lt5"><b>Instruções Gerais</b></font><br><br>
		<b>A) Instruções para Sessão Apresentação Oral:</b> 
		
		<br>- Chegue 10min antes do horário previsto para suas avaliações e compareça na secretaria do evento para retirar seu material;
		<br>- Cada sala terá um membro da equipe de apoio que lhe receberá e entregará seu tablete;
		<br>- A banca será constituída por dois avaliadores, que entre si designam quem irá presidir a sessão;
		<br>- Inicie a avaliação pontualmente no horário previsto. Não podemos ter atrasos;
		<br>- Cada sessão tem horário definido de início e término, todos os alunos de PIBIC e PIBITI, professores orientadores e alunos ouvintes devem assistir ao bloco inteiro, evitando entra e sai na sala;
		<br>- No início de cada apresentação oral, chame o aluno e seu Professor Orientador para que se apresentem;
<br>
		<br>- Sobre a presença do professor orientador, você deverá assinalar na ficha de avaliação:
		<br>( ) professor orientador presente;
		<br>( ) professor ausente, enviou representante e justificativa por escrito;
		<br>( ) professor ausente, enviou representante sem justificativa por escrito;
		<br>( ) professor ausente sem justificativa.
<br>
		<br>- Sobre a presença do estudante, você deverá assinalar na ficha de avaliação:
		<br>( ) estudante presente;
		<br>( ) estudante ausente, trabalho apresentado pelo professor;
		<br>( ) estudante ausente, trabalho não apresentado.
<br>
		<br>- O aluno terá 10 min para apresentar seu trabalho, o arquivo da apresentação já deverá estar na máquina. Caso o aluno coloque nesse momento, este tempo deverá ser computado como parte da apresentação;
		<br>- Transcorrido os 10 min, o senhor terá que interromper o aluno. São dados 5min (cinco minutos) para perguntas e respostas dos avaliadores e público;
		<br>- Elabore perguntas ao aluno;
		<br>- Em seguida, chame o próximo trabalho.
		<br>- Não há horário pré-estabelecido para cada trabalho, podendo ser alterada a ordem se houver motivação;
		<br>- Evite demonstrar cansaço. Ao lado da secretaria do evento, preparamos a sala dos avaliadores com café, suco e petitfours para seu descanso no rápido intervalo;
		<br>- Todos os itens da avaliação devem ser preenchidos;
		<br>- Você poderá salvar todas as avaliações e depois consultá-las novamente se assim desejar;
		<br>- Ao término de todas as avaliações, você deve clicar em "enviar avaliações";
		<br>- Ao término da sessão o senhor deverá devolver o tablete para o membro da equipe. 
		
		<br><br>
		<B>B) Instruções para Avaliador Suplente da Apresentação Oral</B> 
		<br>- Você deverá assumir as avaliações dos trabalhos se após 5min (cinco minutos) do horário estabelecido para início da sessão algum avaliador estiver ausente;
		<br>- A partir desse momento você passou a ser o avaliador principal;
		<br>- Caso os dois avaliadores estejam presentes, você está convidado a permanecer na sala e contribuir no momento das perguntas, no entanto, não atribuirá nota ao estudante. 
		
<br><br>
		<b>C) Instruções para Sessão de Pôster:</b> 
		<br>Serão 4 (quatro) sessões de pôsteres, todas no térreo do bloco verde:
		<br><br> 
		<b><i>Sessão 1</i></b> - dia 06/10 - 10:30 às 12:30h, trabalhos do PIBIC das áreas de Ciências Agrárias e Ciências Sociais Aplicadas.
		<br><br> 
		<b><i>Sessão 2</i></b> - dia 06/10 - 15:30 às 17:30h, trabalhos do PIBIC da área de Ciências Exatas e PIBITI (todas as áreas).
		<br><br> 
		<b><i>Sessão 3</i></b> - dia 07/10 - das 10:30 às 12:30h, trabalhos do PIBIC das áreas de Ciências Humanas e PIBIC Jr.
		<br><br> 
		<b><i>Sessão 4</i></b> - dia 07/10 - das 15:30 às 17:30h, trabalhos do PIBIC da área de Ciências da Vida.
<br>
		<br>- Chegue com 15 min de antecedência dirija-se à secretaria do evento situada no térreo do bloco verde para retirar seu tablete para a avaliação;
		<br>- Quatro integrantes da equipe de apoio (STAFF) estarão circulando pela sessão de pôster para auxiliá-lo em caso de dúvidas e dificuldade na localização dos pôsteres;
		<br>- Cada avaliação de pôster deve durar até 10 min, assim você terá tempo de avaliar todos os trabalhos designados para você;
		<br>- Não perca tempo procurando pôster, peça ajuda a um membro da STAFF;
		<br>- Assinale na sua ficha de avaliação se o pôster está afixado adequadamente ou pôster ausente;
<br>
		<br>- Sobre a presença do professor orientador, você deverá assinalar na ficha de avaliação:
		<br>( ) professor orientador presente;
		<br>( ) professor ausente, enviou representante e justificativa por escrito;
		<br>( ) professor ausente, enviou representante sem justificativa por escrito;
		<br>( ) professor ausente e estudante justificou que o professor está atuando como avaliador;
		<br>( ) professor ausente sem justificativa.
<br>
		<br>- Sobre a presença do estudante, você deverá assinalar na ficha de avaliação:
		<br>( ) estudante presente
		<br>( ) estudante ausente.
<br>
		<br>- Os pôsteres que tiverem o adesivo de INTERCÂMBIO e CIÊNCIA SEM FRONTEIRAS não precisam ser avaliados. Os alunos estão no exterior e não concorrem a premiação;
		<br>- Organize-se com o horário para que dê tempo de avaliar todos os pôsteres. Não deixe trabalhos sem avaliação, os alunos ficam muito frustrados e são prejudicados na premiação dos melhores projetos;
		<br>- Ao término da sessão o senhor deverá devolver o tablete no mesmo local. 
		
		<br><br>
		<b>D) Instruções para Avaliador Suplente da Sessão de Pôster:</b> 
		<br>- Dirija-se à secretaria do evento as 11h para as sessões do período da manhã ou as 16h para as sessões no horário da tarde, nesse momento, já saberemos se houve falta de algum avaliador;
		<br>- Mantenha seu horário disponível até o término da sessão a que foi designado. </td>
