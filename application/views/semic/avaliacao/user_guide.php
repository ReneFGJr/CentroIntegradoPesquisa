		<font class="lt5"><b>Critérios de Avaliação</b></font><br><br>
		<B>CRITÉRIOS DE AVALIAÇÃO DA APRESENTAÇÃO DE PÔSTER</B>
		<br>
		<br><b>Poder de síntese</b>: o estudante foi capaz de apresentar de forma clara e objetiva a questão central da pesquisa, a metodologia desenvolvida, pontos relevantes da discussão e das considerações finais.
		<br>
		<br><b>Contribuição para a formação científica</b>: a pesquisa foi desenvolvida de modo efetivo para que o estudante ingresse no universo da ciência. Foi possível identificar que o estudante depreendeu a partir do percurso realizado os pontos nodais do desenvolvimento de uma pesquisa ?
		<br>
		<br><b>Conteúdo</b>: identificam-se no pôster as etapas da pesquisa que foram desenvolvidas? Estão de acordo com o que é esperado para a metodologia científica e redação científica?
		<br>
		<br><b>Qualidade visual</b>: a qualidade visual do pôster é adequada e valoriza a exposição da pesquisa desenvolvida?
		<br>
		<br><b>Desempenho do Estudante</b>: Soube apresentar as informações relevantes da pesquisa desenvolvida? Inclua neste item: vocabulário e postura adequados; foi capaz responder a arguição do avaliador.
		<br>
		<br><b>Nota geral</b>: Considerando todos os critérios anteriores, dê uma nota de 0 a 10. Se o trabalho tiver mérito para concorrer aos 3 melhores trabalhos de cada grande área (C. Exatas e Engenharias; C. Agrárias; C. da Vida; C. Humanas; C. Sociais Aplicadas), assinale 10 com mérito.	
